# 문제: 제일 작은 수 제거하기 (배열에서 최솟값을 제거하고, 빈 배열이면 -1 반환)
#
# 사용 개념
# min(arr) : 배열(리스트) 내에서 가장 작은 값을 찾아 반환하는 내장 함수
# arr.remove(값) : 배열에서 해당 '값'을 찾아서 직접 삭제 (인덱스가 아닌 값 기준)
# len(arr) == 0 : 배열의 길이가 0인지(비어있는지) 확인하는 조건식
#
# 배운 점
# 1. 리스트의 요소를 삭제할 때, 인덱스(방 번호) 기준이면 pop()을 쓰고 데이터 값 자체를 기준으로 삭제하려면 remove()를 써야 함을 명확히 구분해야 한다.
# 2. 원본 배열(arr)의 데이터가 담긴 상태에서 연산을 수행해야 하며, 새로 만든 빈 배열(answer)에 대고 집계 함수나 삭제 함수를 쓰면 에러가 발생한다.
# 3. 예외 조건(빈 배열일 때 -1 처리)은 최솟값을 제거하는 메인 로직이 수행된 직후에 배열의 길이를 측정하여 분기 처리하는 것이 자연스럽다.
#
# 느낀 점
# 문법 오타를 수정하면서 pop()과 remove()의 결정적인 차이점을 배울 수 있었다. 파이썬이 제공하는 편리한 내장 함수(min, remove)들을 적재적소에 조합하니 복잡한 반복문 없이도 배열을 자유자재로 다룰 수 있어서 흥미로웠다.

def solution(arr):
    arr.remove(min(arr))
    if len(arr) == 0:
        return [-1]
    return arr
