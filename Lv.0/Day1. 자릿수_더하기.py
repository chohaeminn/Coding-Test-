# 문제: 자릿수 더하기

# 사용 문법
# while
# %
# //

# 풀이 포인트
# n % 10으로 마지막 자릿수를 구한다.
# n // 10으로 마지막 자릿수를 제거한다.
# n이 0이 될 때까지 반복하며 자릿수를 더한다.

# 실수한 점
# len(str(n))를 이용해 반복하려고 했다.
# while 조건식을 잘못 작성해 SyntaxError가 발생했다.
# 자릿수 문제는 while n > 0 으로 해결 가능하다.

# 배운 점
# % 10 : 마지막 자릿수 추출
# // 10 : 마지막 자릿수 제거
# 자릿수 관련 문제는 %10, //10 패턴을 자주 사용한다.

# 느낀 점
# 문자열 변환 없이 숫자 연산만으로 자릿수의 합을 구할 수 있었다.
# %와 // 연산에 익숙해질 필요가 있다.

def solution(n):
    answer = 0

    while n > 0:
        answer += n % 10
        n = n // 10

    return answer
