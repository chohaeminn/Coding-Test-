# 문제: 핸드폰 번호 가리기 (뒷 4자리를 제외한 나머지 숫자를 *으로 바꾸기)
#
# 사용 개념
# 변수 대입 : 항상 '변수 = 값' 형태로 좌변에 변수명이 와야 함 (len() = n은 문법 에러)
# 문자열 곱하기 : '*' * N 형태로 작성하면 문자열을 N번만큼 반복하여 생성함
# 문자열 슬라이싱 : phone_number[-4:]를 사용하면 문자열의 뒤에서 4번째 글자부터 끝까지 잘라옴
#
# 배운 점
# 1. 파이썬에서 값을 대입할 때 우변의 값을 좌변의 변수에 할당하는 흐름을 명확히 인지해야 한다.
# 2. 다른 언어와 달리 파이썬은 문자열에 곱하기 연산이 가능하므로, 특정 문자를 반복 출력할 때 for 루프를 쓰지 않아도 간결하게 표현할 수 있다.
# 3. 인덱스에 음수(-4)를 활용하면 뒤에서부터 원하는 위치를 지정해 문자열을 쉽게 슬라이싱할 수 있다.
#
# 느낀 점
# 문법 에러 메시지를 통해 변수 대입의 기본 원칙을 다시금 되새길 수 있었다. for문으로 복잡하게 처리하려던 로직이 파이썬의 문자열 곱하기와 슬라이싱 기능 덕분에 단 한 줄로 깔끔하게 정리되는 것을 보며 파이썬이라는 언어의 편리함을 체감했다.

def solution(phone_number):
    n = len(phone_number)
    answer = '*' * (n - 4) + phone_number[-4:]
    return answer
