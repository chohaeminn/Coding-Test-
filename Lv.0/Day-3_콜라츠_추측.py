# 문제: 콜라츠 추측 (반복 횟수 예외 처리 조건 및 나눗셈 디버깅)
#
# 사용 개념
# answer >= 500 : 작업 반복 횟수가 500번이 넘었는지 체크하는 올바른 예외 처리 조건
# //= 연산자 : 값을 정수형(Integer)으로 안전하게 유지하며 2로 나누기
#
# 배운 점
# 1. 제한 조건(500번)을 검사할 때는 대상 변수가 입력값(num)인지, 횟수 카운터(answer)인지 정확히 구분하여 매칭해야 한다.
# 2. 파이썬 알고리즘 문제에서 숫자의 짝수/홀수를 지속적으로 판별하며 연산할 때는 소수점 오차가 없는 정수 나눗셈(//)을 쓰는 것이 무한 루프를 방지하는 지름길이다.
#
# 느낀 점
# while문의 유지 조건을 찾아낸 것만으로도 큰 발전이었다. 변수의 역할(num은 변하는 숫자, answer는 횟수)을 명확히 정의하고 쓰지 않으면 엉뚱한 조건을 걸 수 있다는 것을 깨달았다. 오답을 수정해 나가는 과정에서 파이썬 문법과 더 친해진 느낌이다.

def solution(num):
    answer = 0
    while (num != 1):
        if answer >= 500:
            return -1
        
        if num % 2 == 0:
            num //= 2
            answer += 1
        else:
            num = num * 3 + 1
            answer += 1
            
    return answer
