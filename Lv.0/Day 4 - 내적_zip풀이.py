# 문제: 두 1차원 배열의 내적 구하기 (zip과 sum을 활용한 최적화 버전)
#
# 사용 개념
# zip(a, b) : 두 배열의 요소를 차례대로 꺼내어 (x, y) 형태로 짝을 지어주는 내장 함수
# Generator Expression : (x * y for x, y in zip(a, b)) 형태로 루프를 돌며 곱셈 결과를 실시간으로 생성
# sum() : 생성된 모든 곱셈 결과들을 하나로 더하여 최종 내적 값을 산출
#
# 배운 점
# 1. 인덱스 번호(range)를 사용하여 배열에 접근하는 방식보다, zip() 함수를 사용하면 인덱스 실수 없이 두 배열의 요소를 안전하게 한 쌍으로 묶어 처리할 수 있다.
# 2. 파이썬의 축약 문법(List Comprehension / Generator Expression)을 sum() 함수와 결합하면 여러 줄의 for 루프와 누적 변수(answer) 선언 없이 단 한 줄로 깔끔하게 집계할 수 있다.
#
# 느낀 점
# 인덱스로 풀었던 복잡한 코드가 파이썬의 내장 함수들을 조합하니 군더더기 없는 단 한 줄의 예술적인 코드로 변하는 것을 보며 감탄했다. 파이썬이라는 언어가 가진 '간결함과 강력함'이 무엇인지 제대로 체감할 수 있었던 의미 있는 문제였다.

def solution(a, b):
    return sum(x * y for x, y in zip(a, b))
