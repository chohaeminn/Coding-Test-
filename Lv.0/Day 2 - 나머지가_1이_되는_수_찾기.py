# 문제 : 나머지가 1이 되는 수 찾기

# 사용 문법
# range() : 반복 범위 생성
# for문 : 2부터 n까지 순차적으로 확인
# % : 나머지 연산자
# return : 조건을 만족하는 값을 즉시 반환

# 배운 점
# 가장 작은 값을 찾는 문제에서는 작은 수부터 순서대로 확인하면 된다.
# 조건을 처음 만족하는 순간 그 값이 최솟값이므로 바로 return 할 수 있다.
# 나머지를 구할 때는 % 연산자를 사용한다.

def solution(n):
    for i in range(2, n + 1):
        if n % i == 1:
            return i
